package com.game;

/**
 * An enumerated type of where a player can pick up from: DECK or PILE or call Yaniv.
 *
 * @author Barry Irvine
 */
public enum Turn {

    /**
     * Pickup from the deck
     */
    PICKUP_DECK,
    /**
     * Pickup from the pile
     */
    PICKUP_PILE,
    /**
     * Call Yaniv
     */
    CALL_YANIV

}
